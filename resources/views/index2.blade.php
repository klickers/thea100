            <p>Anyhow, Richard, one of my best friends (if not <em>the</em> best), convinced me to take an acting class this fall. 
                I'd been entertaining the idea for a while, but never did anything about it until he gave me the final push.</p>
            <p>"Embarrassing yourself is the worst thing that could happen," he told me, "and we both know you're over that."  
                (I stopped being embarrassed about random things a year or two ago.  I decided it wasn't worth my time.)</p>
            <p>Richard loves theater.  He's been acting since he was four, and has been in most of the plays in our local community theater.  
                He loves to discuss Aristotle's <em>Poetics</em> and read plays aloud when he's bored.  
                You might call him my informal theater teacher, since being his friend means that I invariably pick up a few pieces of "theater knowledge" here and there, although I never ask for it.</p>
            <p>He once asked me to sketch out the structure of a plot.  Being the absolutely amazing friend I was and am, I obliged.</p>
            
            <p>Richard took one look at my paper and chuckled.  "Wow, look at all these complicated words you know," he exclaimed.</p>
            <p>"It's not funny," I growled.</p>
            <p>"'Denouement'.  'Exposition' is a big one too.  Guess my knowledge has rubbed off on you?"</p>
            <p>"Has not."  I shoved him.  "I saw it in Jasper's word book."</p>
